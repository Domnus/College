    private void voltar() {
        frmConta windowConta = new frmConta(banco, numeroConta);
        windowConta.setVisible(true);
        this.dispose();
    }

    private void sacar() {
        float saldo = Float.parseFloat(tfValor.getText());
        int op = JOptionPane.showConfirmDialog(this, "Deseja mesmo realizar o saque?");

        if (op == 0) {
            if (banco.sacar(numeroConta, saldo) == false) {
                JOptionPane.showMessageDialog(this, "Saldo insuficiente.");
            } else {
                JOptionPane.showMessageDialog(this, "Dep√≥sito realizado com sucesso!");
                voltar();
            }
        } else if (op == 1) {
            voltar();
        }
    }
